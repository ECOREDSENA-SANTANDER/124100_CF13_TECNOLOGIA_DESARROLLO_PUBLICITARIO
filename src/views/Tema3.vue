    h4 Promociones de ventas según el incentivo ofrecido al consumidor
    p.mb-4 Además de en función del público objetivo al que van dirigidas, el criterio de clasificación de las promociones de ventas según el tipo de incentivo promocional ofrecido es uno de los más empleados. En este sentido, las promociones de ventas se pueden agrupar en diferentes clases atendiendo a las características del incentivo que se ofrece al consumidor, independientemente de que la promoción sea del fabricante, del distribuidor o resultado de una colaboración conjunta entre ambos. Existen cuatro criterios de clasificación de las promociones en función del tipo de incentivo ofrecido al consumidor: en función de la naturaleza del incentivo, en función del horizonte temporal, en función de la certeza del incentivo y, por último, en función del objetivo perseguido.
